const norm = (s) =>
  String(s || "")
    .normalize("NFKC")
    .toLowerCase()
    .trim()
    .replace(/[\s._-]+/g, ""); // collapse separators so "meta-mask" â‰ˆ "metamask"

const RESERVED_SET = new Set(RESERVED_USERNAMES.map(norm));

function isReservedUsername(input) {
  return RESERVED_SET.has(norm(input));
}

module.exports = { isReservedUsername };
